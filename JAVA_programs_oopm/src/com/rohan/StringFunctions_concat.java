package com.rohan;

public class StringFunctions_concat {
	
	//String Immutable in java
	String name = "Rohan";
	
	public void print() {
		System.out.println(name);
		System.out.println(name.concat(" " + "Gire"));
	}
	public static void main(String[] args) {
		
		StringFunctions_concat obj = new StringFunctions_concat();
		obj.print();		
	}
	
}
	
